const ResourcePreview: React.FC<{ resource: Resource }> = ({ resource }) => {
    const [hasError, setHasError] = React.useState(false);

    // Simple image placeholder for all file types
    const placeholder = (
        <div className="w-full h-full bg-[#F9F9F9] flex items-center justify-center overflow-hidden">
            <img
                src={`data:image/svg+xml,${encodeURIComponent(`<svg width="300" height="200" xmlns="http://www.w3.org/2000/svg">
                    <rect width="300" height="200" fill="#F9F9F9"/>
                    <text x="50%" y="50%" font-family="Arial, sans-serif" font-size="14" fill="#535352" text-anchor="middle" dy=".3em">${resource.title}</text>
                </svg>`)}`}
                alt={resource.title}
                className="w-full h-full object-cover"
            />
        </div>
    );

    // Use preview image if available and no error occurred, otherwise use placeholder
    if (resource.previewImage && !hasError) {
        return (
            <div className="w-full h-full bg-[#F9F9F9] flex items-center justify-center overflow-hidden">
                <img
                    src={resource.previewImage}
                    alt={resource.title}
                    className="w-full h-full object-cover"
                    onError={() => setHasError(true)}
                />
            </div>
        );
    }

    return placeholder;
};


